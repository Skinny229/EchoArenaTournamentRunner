package EchoTournament.ClientWrapper.Model;

public class EchoGameGeneratedStats {

    private int points;
    private double possession_time;
    private int interceptions;
    private int blocks;
    private int steals;
    private int catches;
    private int passes;
    private int saves;
    private int goals;
    private int assists;
    private int shots_taken;




    public int getPoints() {
        return points;
    }

    public void setPoints(int points) {
        this.points = points;
    }

    public double getPossession_time() {
        return possession_time;
    }

    public void setPossession_time(double possession_time) {
        this.possession_time = possession_time;
    }

    public int getInterceptions() {
        return interceptions;
    }

    public void setInterceptions(int interceptions) {
        this.interceptions = interceptions;
    }

    public int getBlocks() {
        return blocks;
    }

    public void setBlocks(int blocks) {
        this.blocks = blocks;
    }

    public int getSteals() {
        return steals;
    }

    public void setSteals(int steals) {
        this.steals = steals;
    }

    public int getCatches() {
        return catches;
    }

    public void setCatches(int catches) {
        this.catches = catches;
    }

    public int getPasses() {
        return passes;
    }

    public void setPasses(int passes) {
        this.passes = passes;
    }

    public int getSaves() {
        return saves;
    }

    public void setSaves(int saves) {
        this.saves = saves;
    }

    public int getGoals() {
        return goals;
    }

    public void setGoals(int goals) {
        this.goals = goals;
    }

    public int getAssists() {
        return assists;
    }

    public void setAssists(int assists) {
        this.assists = assists;
    }

    public int getShots_taken() {
        return shots_taken;
    }

    public void setShots_taken(int shots_taken) {
        this.shots_taken = shots_taken;
    }


}
